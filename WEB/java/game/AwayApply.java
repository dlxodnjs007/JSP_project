package game;

public class AwayApply {
    private int apply_id;
    private int game_id_no;
    private String away_id;
    private String a_team_name;
    private String away_people;
    private String away_level;
    private String away_age;
    private int accept_status;
    
    public int getApply_id() {
        return apply_id;
    }
    public void setApply_id(int apply_id) {
        this.apply_id = apply_id;
    }
    public int getGame_id_no() {
        return game_id_no;
    }
    public void setGame_id_no(int game_id_no) {
        this.game_id_no = game_id_no;
    }
    public String getAway_id() {
        return away_id;
    }
    public void setAway_id(String away_id) {
        this.away_id = away_id;
    }
    public String getA_team_name() {
        return a_team_name;
    }
    public void setA_team_name(String a_team_name) {
        this.a_team_name = a_team_name;
    }
    public String getAway_people() {
        return away_people;
    }
    public void setAway_people(String away_people) {
        this.away_people = away_people;
    }
    public String getAway_level() {
        return away_level;
    }
    public void setAway_level(String away_level) {
        this.away_level = away_level;
    }
    public String getAway_age() {
        return away_age;
    }
    public void setAway_age(String away_age) {
        this.away_age = away_age;
    }
    public int getAccept_status() {
        return accept_status;
    }
    public void setAccept_status(int accept_status) {
        this.accept_status = accept_status;
    }
}
