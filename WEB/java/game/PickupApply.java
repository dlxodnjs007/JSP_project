package game;

public class PickupApply {
    private int apply_id;
    private int game_id_no;
    private String name;
    private String guest_id;
    private String height;
    private String position;
    private String age;
    private int accept_status;
    public int getApply_id() {
        return apply_id;
    }
    public void setApply_id(int apply_id) {
        this.apply_id = apply_id;
    }
    public int getGame_id_no() {
        return game_id_no;
    }
    public void setGame_id_no(int game_id_no) {
        this.game_id_no = game_id_no;
    }
    public String getName() {
        return name;
    }
    public void setName(String name) {
        this.name = name;
    }
    public String getGuest_id() {
        return guest_id;
    }
    public void setGuest_id(String guest_id) {
        this.guest_id = guest_id;
    }
    public String getHeight() {
        return height;
    }
    public void setHeight(String height) {
        this.height = height;
    }
    public String getPosition() {
        return position;
    }
    public void setPosition(String position) {
        this.position = position;
    }
    public String getAge() {
        return age;
    }
    public void setAge(String age) {
        this.age = age;
    }
    public int getAccept_status() {
        return accept_status;
    }
    public void setAccept_status(int accept_status) {
        this.accept_status = accept_status;
    }
    
}
